#!/usr/bin/env node
//
// Repair Wave 8 seed transcripts that transcriptapi.com returned in the
// wrong language. We discovered post-bootstrap that 4 of 12 seed JSONs
// in git history (commit 11ec484) actually contain Arabic or Albanian
// content — transcriptapi picked translation tracks instead of the
// English originals when we built Wave 8.
//
// This script runs on the user's local machine (NOT Vercel — YouTube
// IP-blocks Vercel datacenter ranges). It hits YouTube's public
// timedtext endpoint with explicit lang=en, normalizes the response
// into our transcript schema, and writes directly to Upstash KV
// (and optionally to the git-tracked seed JSON for future bootstraps).
//
// Usage:
//   UPSTASH_REDIS_REST_URL=... UPSTASH_REDIS_REST_TOKEN=... \
//     node scripts/repair-yt-seeds.mjs
//
//   # Dry-run (don't write to KV, just print what we'd cache):
//   ... node scripts/repair-yt-seeds.mjs --dry
//
//   # Also overwrite the source JSON in data/transcripts/ so future
//   # bootstraps don't re-poison the cache:
//   ... node scripts/repair-yt-seeds.mjs --write-files
//
// Requires Node 18+ (native fetch).
//

import fs from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

// Use the in-house YouTube captions fetcher. It's "dead in prod" because
// YouTube IP-blocks Vercel datacenter ranges, but works fine from a
// residential IP. Handles the signed-URL dance (watch page → parse
// ytInitialPlayerResponse → pick English non-ASR track → fetch
// track.baseUrl with fmt=json3).
import { getYouTubeCaptions } from '../lib/youtube-captions.js'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const REPO_ROOT = path.resolve(__dirname, '..')
const TX_DIR = path.join(REPO_ROOT, 'data', 'transcripts')

const args = process.argv.slice(2)
const DRY = args.includes('--dry')
const WRITE_FILES = args.includes('--write-files')

// The 4 known-bad seeds. Add more here if we find others later.
// (slug, videoId, expected lang) — expected is just a label, the
// real language we request from YouTube is always 'en' for these.
const BAD_SEEDS = [
  ['ted-how-great-leaders-inspire-action-simon-sinek',         'u4ZoJKF_VuA'],
  ['ted-inside-mind-master-procrastinator-tim-urban',          'arj7oStGLkU'],
  ['ted-looks-arent-everything-believe-me-im-a-model-cameron-russell', 'iCvmsMzlF7o'],
  ['ted-the-happy-secret-to-better-work-shawn-achor',          'RcGyVTAoXEU'],
]

const CACHE_KEY = (vid) => `yt:transcript:${vid}`

// ---------- YouTube captions via in-house fetcher ----------

async function fetchEnglishCaptions(videoId) {
  // Delegates to lib/youtube-captions.js which:
  //   1. Fetches the YouTube watch page HTML
  //   2. Parses ytInitialPlayerResponse to find the caption tracks list
  //   3. Picks English non-ASR (then English ASR fallback, then any other)
  //   4. Fetches the chosen track's signed baseUrl with fmt=json3
  //
  // Naked timedtext.googleusercontent.com calls stopped working a couple
  // years back — YouTube now requires signed URLs that you can only get
  // by parsing the watch page first. lib/youtube-captions.js handles
  // that dance.
  const url = `https://www.youtube.com/watch?v=${videoId}`
  try {
    const result = await getYouTubeCaptions(url, { preferLang: 'en' })
    if (!result?.segments?.length) return null
    return {
      segments: result.segments,
      languageCode: result.languageCode,
      isAutoGenerated: !!result.isAutoGenerated,
      title: result.title,
      author: result.author,
      durationSec: result.durationSec,
      attempt: `lib/youtube-captions (lang=${result.languageCode})`,
    }
  } catch (err) {
    console.error(`    err: ${err.code || err.name}: ${err.message?.slice(0, 120)}`)
    return null
  }
}

// ---------- Upstash KV ----------

async function getRedis() {
  if (DRY) return null
  if (!process.env.UPSTASH_REDIS_REST_URL || !process.env.UPSTASH_REDIS_REST_TOKEN) {
    console.error('Missing UPSTASH_REDIS_REST_URL or UPSTASH_REDIS_REST_TOKEN env vars.')
    console.error('Run with --dry to test without writing to KV.')
    process.exit(1)
  }
  const { Redis } = await import('@upstash/redis')
  return new Redis({
    url: process.env.UPSTASH_REDIS_REST_URL,
    token: process.env.UPSTASH_REDIS_REST_TOKEN,
  })
}

// ---------- main ----------

async function main() {
  const redis = await getRedis()

  let ok = 0
  let fail = 0

  for (const [slug, videoId] of BAD_SEEDS) {
    process.stdout.write(`  - ${slug} (${videoId}) ... `)
    const result = await fetchEnglishCaptions(videoId)
    if (!result) {
      console.log('FAILED — no English captions returned by any variant')
      fail++
      continue
    }
    const { segments, attempt, languageCode, isAutoGenerated } = result
    const text = segments.map(s => s.text).join(' ').replace(/\s+/g, ' ').trim()

    // Load the existing seed JSON to preserve title/author/etc.
    const filePath = path.join(TX_DIR, `${slug}.json`)
    let seed = null
    try {
      seed = JSON.parse(await fs.readFile(filePath, 'utf-8'))
    } catch {
      // File might have been stripped (the copyright-pivot commit
      // removed segments+text and added empty quotes:[]). Still works.
      seed = {}
    }

    // Build the cache payload (matches /api/youtube-captions shape).
    // Prefer fresh metadata from YouTube; fall back to whatever the seed
    // JSON already had (titles set by us are usually nicer than YT's).
    const payload = {
      videoId,
      title: seed.title || result.title || `YouTube ${videoId}`,
      author: seed.author || result.author || '',
      durationSec: seed.durationSec || result.durationSec || null,
      languageCode: languageCode || 'en',
      languageName: undefined,
      isAutoGenerated,
      segments,
      text,
      provider: 'youtube-timedtext-repair',
    }

    console.log(`ok (${segments.length} segments, ${text.length} chars, via ${attempt}${isAutoGenerated ? ' [ASR]' : ''})`)

    if (!DRY) {
      // Plain SET (overwrite). The cache might have NX-protected runtime
      // garbage from before the fix; we want to wipe and replace.
      await redis.del(CACHE_KEY(videoId))
      await redis.set(CACHE_KEY(videoId), JSON.stringify(payload))
    }

    if (WRITE_FILES) {
      // Persist back to data/transcripts/{slug}.json so future bootstraps
      // (e.g. on a fresh Upstash instance) pull the English version.
      // Preserves all the existing fields the page template reads.
      const merged = {
        ...seed,
        videoId,
        languageCode: 'en',
        language: 'en',
        segments,
        text,
        provider: 'youtube-timedtext-repair',
        repairedAt: new Date().toISOString(),
      }
      await fs.writeFile(filePath, JSON.stringify(merged, null, 2) + '\n')
    }
    ok++
  }

  console.log(`\nDone. ok=${ok}, failed=${fail}${DRY ? ' (DRY RUN — no KV writes)' : ''}${WRITE_FILES ? ' [files updated]' : ''}`)
  if (fail) {
    console.log('\nFor any failed videos, you can manually:')
    console.log('  1. Open https://www.youtube.com/watch?v=VIDEOID in a browser')
    console.log('  2. Click the [...] menu under the video → "Show transcript"')
    console.log('  3. Select "English" from the language dropdown')
    console.log('  4. Copy-paste segments into a JSON file and re-run bootstrap.')
  }
}

main().catch(e => {
  console.error('Fatal:', e)
  process.exit(1)
})
