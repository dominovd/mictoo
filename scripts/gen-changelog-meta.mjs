#!/usr/bin/env node
//
// Build-time generator: reads data/changelog.json, writes the latest
// entry's date to lib/changelog-meta-gen.js as a static const.
//
// Why: SiteHeader needs the latest changelog date to compare against
// localStorage 'mictoo:changelog_lastSeen' for its red-dot indicator.
// Before 2026-06-05 it fetched /api/changelog-meta on every page load,
// which Vercel analytics showed at 1.1k/day → 33k/mo function
// invocations for a value that changes once or twice a week.
//
// Generating a static module at build time means the value is in the
// JS bundle, no network call, no function invocation, no Vercel cost.
// The const updates whenever you run `npm run build` (which is every
// Vercel deploy).
//
// Trade-off: if you edit data/changelog.json and don't rebuild, the
// indicator stays based on the old date. That's fine — same was already
// true for the /whats-new page (it's SSG).

import fs from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const REPO_ROOT = path.resolve(__dirname, '..')
const SOURCE = path.join(REPO_ROOT, 'data', 'changelog.json')
const OUT = path.join(REPO_ROOT, 'lib', 'changelog-meta-gen.js')

async function main() {
  let latest = null
  try {
    const raw = await fs.readFile(SOURCE, 'utf-8')
    const data = JSON.parse(raw)
    const dates = (data?.entries || [])
      .map((e) => e?.date)
      .filter(Boolean)
      .sort()
    latest = dates[dates.length - 1] || null
  } catch (err) {
    console.warn('[gen-changelog-meta] could not read changelog.json:', err.message)
  }

  const contents = `// AUTO-GENERATED by scripts/gen-changelog-meta.mjs — do not edit by hand.
// Regenerated on every \`npm run build\`. Source: data/changelog.json
// Used by components/SiteHeader.js for the red-dot unread indicator.
export const LATEST_CHANGELOG_DATE = ${JSON.stringify(latest)}
`
  await fs.writeFile(OUT, contents)
  console.log(`[gen-changelog-meta] wrote ${OUT}: LATEST_CHANGELOG_DATE = ${JSON.stringify(latest)}`)
}

main().catch((err) => {
  console.error('[gen-changelog-meta] failed:', err)
  process.exit(1)
})
