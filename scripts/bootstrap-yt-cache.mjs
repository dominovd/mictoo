#!/usr/bin/env node
//
// Bootstrap the Upstash KV cache with the 12 transcripts we already
// have (in git history before they were stripped in commit c630e87).
// Run this once after deploying the cache layer so the "Get the full
// transcript" CTA on /transcripts/{slug} pages hits the cache from the
// very first user — zero transcriptapi credits consumed for those 12.
//
// Reads from git history:
//   git show 11ec484:data/transcripts/<slug>.json
// (commit 11ec484 is where the 12 seed transcripts were committed with
// full segments. 01e8d81 was only the scaffold; c630e87 stripped them.)
//
// Usage:
//   UPSTASH_REDIS_REST_URL=... UPSTASH_REDIS_REST_TOKEN=... \
//     node scripts/bootstrap-yt-cache.mjs
//
//   # Dry-run (don't write to KV, just report what would be cached):
//   ... node scripts/bootstrap-yt-cache.mjs --dry
//
//   # Override the source commit (in case the default rolls forward):
//   ... node scripts/bootstrap-yt-cache.mjs --commit=11ec484
//
// Idempotent — safe to re-run. Existing cache entries are overwritten
// with the git-history version (which is fine — the upstream content
// doesn't change for public videos).

import { execSync } from 'node:child_process'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const REPO_ROOT = path.resolve(__dirname, '..')

const args = process.argv.slice(2)
const DRY = args.includes('--dry')
const commitArg = args.find(a => a.startsWith('--commit='))
const SOURCE_COMMIT = commitArg ? commitArg.split('=')[1] : '11ec484'

const CACHE_KEY = (vid) => `yt:transcript:${vid}`

// Discover which transcripts existed at SOURCE_COMMIT.
function listSeedSlugs() {
  try {
    const out = execSync(`git ls-tree --name-only ${SOURCE_COMMIT} data/transcripts/`, {
      cwd: REPO_ROOT,
      encoding: 'utf-8',
    })
    return out
      .split('\n')
      .filter(line => line.endsWith('.json') && !line.includes('/_'))
      .map(line => path.basename(line, '.json'))
  } catch (e) {
    console.error(`Couldn't read tree at ${SOURCE_COMMIT}:`, e.message)
    process.exit(1)
  }
}

function loadFromGit(slug) {
  try {
    const raw = execSync(`git show ${SOURCE_COMMIT}:data/transcripts/${slug}.json`, {
      cwd: REPO_ROOT,
      encoding: 'utf-8',
    })
    return JSON.parse(raw)
  } catch (e) {
    console.error(`  ${slug}: failed to read from git — ${e.message}`)
    return null
  }
}

// Translate a seed JSON entry into the shape /api/youtube-captions
// returns, which is what the cache layer expects to read back.
function toCachePayload(seed) {
  return {
    videoId: seed.videoId,
    title: seed.title,
    author: seed.author || '',
    durationSec: seed.durationSec || null,
    languageCode: seed.language || 'en',
    languageName: undefined,
    isAutoGenerated: false,
    segments: seed.segments || [],
    text: seed.text || '',
    provider: seed.provider || 'transcriptapi',
  }
}

async function main() {
  const slugs = listSeedSlugs()
  console.log(`Found ${slugs.length} seed transcripts at ${SOURCE_COMMIT}`)

  if (!DRY) {
    if (!process.env.UPSTASH_REDIS_REST_URL || !process.env.UPSTASH_REDIS_REST_TOKEN) {
      console.error('Missing UPSTASH_REDIS_REST_URL or UPSTASH_REDIS_REST_TOKEN env vars.')
      console.error('Run with --dry to test without writing.')
      process.exit(1)
    }
  }

  const { Redis } = DRY ? { Redis: null } : await import('@upstash/redis')
  const redis = DRY
    ? null
    : new Redis({
        url: process.env.UPSTASH_REDIS_REST_URL,
        token: process.env.UPSTASH_REDIS_REST_TOKEN,
      })

  let ok = 0
  let skip = 0
  let fail = 0

  for (const slug of slugs) {
    const seed = loadFromGit(slug)
    if (!seed) { fail++; continue }
    if (!seed.segments?.length) {
      console.log(`  - skip ${slug} (no segments in source)`)
      skip++
      continue
    }
    const payload = toCachePayload(seed)
    const key = CACHE_KEY(seed.videoId)
    if (DRY) {
      console.log(`  - would cache ${slug} → ${key} (${seed.segments.length} segments, ${(seed.text || '').length} chars)`)
    } else {
      try {
        // Plain SET (no NX) — bootstrap is the source of truth for the
        // 12 seed videos. Always overwrites whatever runtime may have
        // written. Counterpart: route.js setCachedTranscript uses
        // SET NX so runtime can never trample bootstrap data.
        await redis.set(key, JSON.stringify(payload))
        console.log(`  - cached ${slug} → ${key}`)
      } catch (e) {
        console.error(`  - FAILED ${slug}: ${e.message}`)
        fail++
        continue
      }
    }
    ok++
  }

  console.log(`\nDone. ok=${ok}, skipped=${skip}, failed=${fail}${DRY ? ' (DRY RUN — no writes)' : ''}`)
}

main().catch(e => {
  console.error('Fatal:', e)
  process.exit(1)
})
